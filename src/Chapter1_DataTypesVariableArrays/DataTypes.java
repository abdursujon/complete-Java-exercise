package Chapter1_DataTypesVariableArrays;

public class DataTypes {
    // Primitive data types

    // Integers byte, short, int, long
    byte b = 124; // width 8
    short s = -32767; // width 16
    int i = -2147398383; // width 32
    long l = 8339939355555555593L; // width 64 bits

    //Floating point numbers float and double
    float f = 7777777.999f; // width 32
    double d = 4777777744444444444.2d; // width 64

    // Characters
    char a = 'A';

    // Boolean
    boolean isPrime = true;
}
